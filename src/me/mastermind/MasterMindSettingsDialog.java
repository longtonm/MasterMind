    private void OKButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKButtonActionPerformed
        boolean autoGuess = computerGuessRButton.isSelected();
        int chooser;
        if (userWordRButton.isSelected()) chooser = MMSettingsState.USER_WORD;
        else if (randomWordRButton.isSelected()) chooser = MMSettingsState.RANDOM_WORD;
        else chooser = MMSettingsState.SECRET_WORD;
        chosenSettings = new MMSettingsState(lengthSlider.getValue(), chooser, autoGuess);
        this.setVisible(false);
    }//GEN-LAST:event_OKButtonActionPerformed

