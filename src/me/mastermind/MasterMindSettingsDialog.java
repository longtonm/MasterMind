    public MasterMindSettingsDialog(java.awt.Frame parent, boolean modal, MMSettingsState initPrefs) {
        super(parent, modal);
        initComponents();
        chosenSettings = null;
        this.lengthSlider.setValue(initPrefs.length);
        if (initPrefs.autoGuess) {
            this.computerGuessRButton.doClick();
        }
        else {
            this.userGuessRButton.doClick();
        }
        switch (initPrefs.wordChooser) {
            case MMSettingsState.USER_WORD:
                this.userWordRButton.doClick();
                break;
            case MMSettingsState.RANDOM_WORD:
                this.randomWordRButton.doClick();
                break;
            case MMSettingsState.SECRET_WORD:
                this.secretWordRButton.doClick();
                break;
        }
    }

