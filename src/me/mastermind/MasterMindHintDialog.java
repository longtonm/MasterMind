    public void makeWordPane(Possibility source) {
        if (source == null) {
            wordList.setListData(new String[0]);
        }
        else {
            List<String> wordsForSource = dict.lookup(source);
            wordList.setListData(wordsForSource.toArray(new String[0]));
        }
    }

