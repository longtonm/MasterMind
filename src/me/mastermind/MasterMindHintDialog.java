    private void wordListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_wordListValueChanged
        //OKButton.setEnabled(!wordList.isSelectionEmpty());
        if (!wordList.isSelectionEmpty()) {
            OKButton.setEnabled(true);
            OKButton.requestFocusInWindow();
        }
        else OKButton.setEnabled(false);
    }//GEN-LAST:event_wordListValueChanged

    private void possibilityListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_possibilityListValueChanged
        makeWordPane(possibilityList.getSelectedValue());
        wordList.setEnabled(possibilityList.getSelectedValue() != null);
        if (wordList.getModel().getSize() == 1) {
            wordList.setSelectedIndex(0);
        }
    }//GEN-LAST:event_possibilityListValueChanged

